package com.gnomon.intergration;

/**
 * @author frank
 *
 */
public class IntergrationException extends RuntimeException {

	private static final long serialVersionUID = -531581375082719021L;

	public IntergrationException() {
		super();
	}

	public IntergrationException(String message, Throwable cause) {
		super(message, cause);
	}

	public IntergrationException(String message) {
		super(message);
	}

	public IntergrationException(Throwable cause) {
		super(cause);
	}

}
