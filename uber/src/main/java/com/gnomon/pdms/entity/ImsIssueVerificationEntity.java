package com.gnomon.pdms.entity;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.gnomon.common.base.StringIdEntity;

@Entity
@Table(name="IMS_ISSUE_VERIFICATION")
public class ImsIssueVerificationEntity extends StringIdEntity {

	//顺序
	@Column(name="SEQ") 
	private Long seq;
		
	//问题ID
	@Column(name="ISSUE_ID") 
	private String issueId;

	//验证方案
	@Column(name="VERIFICATION") 
	private String verification;

	//通过要求
	@Column(name="PASS_REQUEST") 
	private String passRequest;

	//实施车辆
	@Column(name="IMPLEMENT_VEHICLE") 
	private String implementVehicle;

	//验证部门
	@Column(name="DEPT_ID") 
	private String deptId;

	//验证工程师
	@Column(name="VERIFICATION_ENGINEER") 
	private String verificationEngineer;

	//验证效果
	@Column(name="VERIFICATION_RESULT") 
	private String verificationResult;

	//效果确认
	@Column(name="EFFECT_CONFIRMATION") 
	private String effectConfirmation;

	//是否更新DFMEA
	@Column(name="IS_UPDATED_DFMEA") 
	private String isUpdatedDfmea;

	//是否更新技术条件
	@Column(name="IS_UPDATED_TC") 
	private String isUpdatedTc;

	//设变通知单（ECO）编号
	@Column(name="ECO_NO") 
	private String ecoNo;

	//
	@Column(name="CREATE_BY") 
	private String createBy;

	//
	@Column(name="CREATE_DATE") 
	private Date createDate;

	//
	@Column(name="UPDATE_BY") 
	private String updateBy;

	//
	@Column(name="UPDATE_DATE") 
	private Date updateDate;
	
	//理由
	@Column(name="REASON") 
	private String reason;
	
	//任务ID
	@Column(name="TASK_ID") 
	private String taskId;
	
	//
	@Column(name="STEPS") 
	private String steps;
	
	//验证部门专业经理
	@Column(name="V_FM") 
	private String vFm;
	
	//验证部门科长
	@Column(name="V_LINE_MGR") 
	private String vLineMgr;
	
	@Column(name="STATUS_CODE") 
	private String statusCode;
	
	@Column(name="ACTUAL_FINISH_DATE") 
	private Date actualFinishDate;
	
	
	public String getIssueId() {
		return issueId;
	}

	public void setIssueId(String issueId) {
		this.issueId = issueId;
	}

	public String getVerification() {
		return verification;
	}

	public void setVerification(String verification) {
		this.verification = verification;
	}

	public String getPassRequest() {
		return passRequest;
	}

	public void setPassRequest(String passRequest) {
		this.passRequest = passRequest;
	}

	public String getImplementVehicle() {
		return implementVehicle;
	}

	public void setImplementVehicle(String implementVehicle) {
		this.implementVehicle = implementVehicle;
	}

	public String getDeptId() {
		return deptId;
	}

	public void setDeptId(String deptId) {
		this.deptId = deptId;
	}

	public String getVerificationEngineer() {
		return verificationEngineer;
	}

	public void setVerificationEngineer(String verificationEngineer) {
		this.verificationEngineer = verificationEngineer;
	}

	public String getVerificationResult() {
		return verificationResult;
	}

	public void setVerificationResult(String verificationResult) {
		this.verificationResult = verificationResult;
	}

	public String getEffectConfirmation() {
		return effectConfirmation;
	}

	public void setEffectConfirmation(String effectConfirmation) {
		this.effectConfirmation = effectConfirmation;
	}

	public String getIsUpdatedDfmea() {
		return isUpdatedDfmea;
	}

	public void setIsUpdatedDfmea(String isUpdatedDfmea) {
		this.isUpdatedDfmea = isUpdatedDfmea;
	}

	public String getIsUpdatedTc() {
		return isUpdatedTc;
	}

	public void setIsUpdatedTc(String isUpdatedTc) {
		this.isUpdatedTc = isUpdatedTc;
	}

	public String getEcoNo() {
		return ecoNo;
	}

	public void setEcoNo(String ecoNo) {
		this.ecoNo = ecoNo;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getUpdateBy() {
		return updateBy;
	}

	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}

	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public Long getSeq() {
		return seq;
	}

	public void setSeq(Long seq) {
		this.seq = seq;
	}

	public String getTaskId() {
		return taskId;
	}

	public void setTaskId(String taskId) {
		this.taskId = taskId;
	}

	public String getSteps() {
		return steps;
	}

	public void setSteps(String steps) {
		this.steps = steps;
	}

	public String getvFm() {
		return vFm;
	}

	public void setvFm(String vFm) {
		this.vFm = vFm;
	}

	public String getvLineMgr() {
		return vLineMgr;
	}

	public void setvLineMgr(String vLineMgr) {
		this.vLineMgr = vLineMgr;
	}

	public String getStatusCode() {
		return statusCode;
	}

	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}

	public Date getActualFinishDate() {
		return actualFinishDate;
	}

	public void setActualFinishDate(Date actualFinishDate) {
		this.actualFinishDate = actualFinishDate;
	}

}
