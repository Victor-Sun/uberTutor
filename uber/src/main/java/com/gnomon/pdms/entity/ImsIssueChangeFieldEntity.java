package com.gnomon.pdms.entity;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.gnomon.common.base.StringIdEntity;

@Entity
@Table(name = "IMS_ISSUE_CHANGE_FIELD")
public class ImsIssueChangeFieldEntity extends StringIdEntity {
	
	//
	@Column(name = "ISSUE_ID")
	private String issueId;
	//
	@Column(name = "FIELD_NAME")
	private String fieldName;
	//
	@Column(name = "FIELD_ORIGINAL_VALUE")
	private String fieldOriginalValue;
	//
	@Column(name = "FIELD_NEW_VALUE")
	private String fieldNewValue;
	//处理人
	@Column(name = "CHANGE_USERID")
	private String changeUserid;
	//处理时间
	@Column(name = "CHANGE_DATE")
	private Date changeDate;
	//
	@Column(name = "CREATE_BY")
	private String createBy;
	//
	@Column(name = "CREATE_DATE")
	private Date createDate;
	//
	@Column(name = "UPDATE_BY")
	private String updateBy;
	//
	@Column(name = "UPDATE_DATE")
	private Date updateDate;
	public String getIssueId() {
		return issueId;
	}
	public void setIssueId(String issueId) {
		this.issueId = issueId;
	}
	public String getFieldName() {
		return fieldName;
	}
	public void setFieldName(String fieldName) {
		this.fieldName = fieldName;
	}
	public String getFieldOriginalValue() {
		return fieldOriginalValue;
	}
	public void setFieldOriginalValue(String fieldOriginalValue) {
		this.fieldOriginalValue = fieldOriginalValue;
	}
	public String getFieldNewValue() {
		return fieldNewValue;
	}
	public void setFieldNewValue(String fieldNewValue) {
		this.fieldNewValue = fieldNewValue;
	}
	public String getChangeUserid() {
		return changeUserid;
	}
	public void setChangeUserid(String changeUserid) {
		this.changeUserid = changeUserid;
	}
	public Date getChangeDate() {
		return changeDate;
	}
	public void setChangeDate(Date changeDate) {
		this.changeDate = changeDate;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public String getUpdateBy() {
		return updateBy;
	}
	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}
	public Date getUpdateDate() {
		return updateDate;
	}
	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}
	
}
