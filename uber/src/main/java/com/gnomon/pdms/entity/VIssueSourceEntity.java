package com.gnomon.pdms.entity;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.gnomon.common.base.StringIdEntity;
@Entity
@Table(name="V_PM_ISSUE_SOURCE")
public class VIssueSourceEntity extends StringIdEntity {

	//项目ID
	@Column(name="PROGRAM_ID") 
	private String programId;

	//问题类型CODE
	@Column(name="ISSUE_TYPE_CODE") 
	private String issueTypeCode;
	
	//问题来源ID
	@Column(name="ISSUE_SOURCE_ID") 
	private String issueSourceId;

	//提出人
	@Column(name="PROPOSED_BY") 
	private String proposedBy;

	//提出日期
	@Column(name="PROPOSED_DATE") 
	private Date proposedDate;

	//问题描述
	@Column(name="ISSUE_DESCRIPTION") 
	private String issueDescription;

	//原因
	@Column(name="REASON") 
	private String reason;

	//工作进展描述
	@Column(name="PROGRESS_DESCRIPTION") 
	private String progressDescription;

	//责任组ID
	@Column(name="RESP_OBS_ID") 
	private String respObsId;

	//责任人ID
	@Column(name="RESP_USER_ID") 
	private String respUserId;

	//完成状态
	@Column(name="ISSUE_COMPLETE_STATUS_CODE") 
	private String issueCompleteStatusCode;

	//计划完成日期
	@Column(name="DUE_DATE") 
	private Date dueDate;

	//实际完成日期
	@Column(name="ACTUAL_FINISH_DATE") 
	private Date actualFinishDate;

	//偏离计划完成天数
	@Column(name="DEPARTED_DAYS") 
	private Long departedDays;

	//
	@Column(name="ISSUE_SOURCE_NAME") 
	private String issueSourceName;

	public String getProgramId() {
		return programId;
	}

	public void setProgramId(String programId) {
		this.programId = programId;
	}

	public String getIssueTypeCode() {
		return issueTypeCode;
	}

	public void setIssueTypeCode(String issueTypeCode) {
		this.issueTypeCode = issueTypeCode;
	}

	public String getIssueSourceId() {
		return issueSourceId;
	}

	public void setIssueSourceId(String issueSourceId) {
		this.issueSourceId = issueSourceId;
	}

	public String getProposedBy() {
		return proposedBy;
	}

	public void setProposedBy(String proposedBy) {
		this.proposedBy = proposedBy;
	}

	public Date getProposedDate() {
		return proposedDate;
	}

	public void setProposedDate(Date proposedDate) {
		this.proposedDate = proposedDate;
	}

	public String getIssueDescription() {
		return issueDescription;
	}

	public void setIssueDescription(String issueDescription) {
		this.issueDescription = issueDescription;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getProgressDescription() {
		return progressDescription;
	}

	public void setProgressDescription(String progressDescription) {
		this.progressDescription = progressDescription;
	}

	public String getRespObsId() {
		return respObsId;
	}

	public void setRespObsId(String respObsId) {
		this.respObsId = respObsId;
	}

	public String getRespUserId() {
		return respUserId;
	}

	public void setRespUserId(String respUserId) {
		this.respUserId = respUserId;
	}

	public String getIssueCompleteStatusCode() {
		return issueCompleteStatusCode;
	}

	public void setIssueCompleteStatusCode(String issueCompleteStatusCode) {
		this.issueCompleteStatusCode = issueCompleteStatusCode;
	}

	public Date getDueDate() {
		return dueDate;
	}

	public void setDueDate(Date dueDate) {
		this.dueDate = dueDate;
	}

	public Date getActualFinishDate() {
		return actualFinishDate;
	}

	public void setActualFinishDate(Date actualFinishDate) {
		this.actualFinishDate = actualFinishDate;
	}

	public Long getDepartedDays() {
		return departedDays;
	}

	public void setDepartedDays(Long departedDays) {
		this.departedDays = departedDays;
	}

	public String getIssueSourceName() {
		return issueSourceName;
	}

	public void setIssueSourceName(String issueSourceName) {
		this.issueSourceName = issueSourceName;
	}

	

}
