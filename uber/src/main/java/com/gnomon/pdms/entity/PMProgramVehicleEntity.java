package com.gnomon.pdms.entity;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.gnomon.common.base.StringIdEntity;

@Entity
@Table(name="PM_PROGRAM_VEHICLE")
public class PMProgramVehicleEntity extends StringIdEntity {
	
	//项目标识(UUID)
	@Column(name="PROGRAM_ID") 
	private String programId;
	
	//车型代码
	@Column(name="VEHICLE_CODE") 
	private String vehicleCode;
	
	//车型名称
	@Column(name="VEHICLE_NAME") 
	private String vehicleName;
	
	//SOP日期
	@Column(name="SOP_DATE") 
	private Date sopDate;
	
	//创建人
	@Column(name="CREATE_BY") 
	private String createBy;
	
	//创建时间
	@Column(name="CREATE_DATE") 
	private Date createDate;
	
	//修改人
	@Column(name="UPDATE_BY") 
	private String updateBy;

	//修改时间
	@Column(name="UPDATE_DATE") 
	private Date updateDate;
	
	//删除人
	@Column(name="DELETE_BY") 
	private String deleteBy;

	//删除时间
	@Column(name="DELETE_DATE") 
	private Date deleteDate;
	
	//项目经理
	@Column(name="PM") 
	private String pm;
	
	
	//项目总监
	@Column(name="DIRECTOR") 
	private String director;
	
	//质量经理
	@Column(name="QM") 
	private String qm;
	
	//计划开始时间
	@Column(name="PLANNED_START_DATE") 
	private Date plannedStartDate;
	
	//计划结束时间
	@Column(name="PLANNED_FINISH_DATE") 
	private Date plannedFinishDate;
	
	// 状态
	@Column(name="STATUS_CODE") 
	private String statusCode;

	public String getProgramId() {
		return programId;
	}

	public String getPm() {
		return pm;
	}

	public Date getPlannedStartDate() {
		return plannedStartDate;
	}

	public void setPlannedStartDate(Date plannedStartDate) {
		this.plannedStartDate = plannedStartDate;
	}

	public Date getPlannedFinishDate() {
		return plannedFinishDate;
	}

	public void setPlannedFinishDate(Date plannedFinishDate) {
		this.plannedFinishDate = plannedFinishDate;
	}

	public void setPm(String pm) {
		this.pm = pm;
	}

	public String getDirector() {
		return director;
	}

	public void setDirector(String director) {
		this.director = director;
	}

	public String getQm() {
		return qm;
	}

	public void setQm(String qm) {
		this.qm = qm;
	}

	public void setProgramId(String programId) {
		this.programId = programId;
	}

	public String getVehicleCode() {
		return vehicleCode;
	}

	public void setVehicleCode(String vehicleCode) {
		this.vehicleCode = vehicleCode;
	}

	public String getVehicleName() {
		return vehicleName;
	}

	public void setVehicleName(String vehicleName) {
		this.vehicleName = vehicleName;
	}

	public Date getSopDate() {
		return sopDate;
	}

	public void setSopDate(Date sopDate) {
		this.sopDate = sopDate;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getUpdateBy() {
		return updateBy;
	}

	public void setUpdateBy(String updateBy) {
		this.updateBy = updateBy;
	}

	public Date getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(Date updateDate) {
		this.updateDate = updateDate;
	}

	public String getDeleteBy() {
		return deleteBy;
	}

	public void setDeleteBy(String deleteBy) {
		this.deleteBy = deleteBy;
	}

	public Date getDeleteDate() {
		return deleteDate;
	}

	public void setDeleteDate(Date deleteDate) {
		this.deleteDate = deleteDate;
	}

	public String getStatusCode() {
		return statusCode;
	}

	public void setStatusCode(String statusCode) {
		this.statusCode = statusCode;
	}

}
